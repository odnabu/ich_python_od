    # def __le__(self, other):
    #     return self.price <= other.price

    # def __ge__(self, other):
    #     return self.price >= other.price

    def __bool__(self):
        return bool(self.price)

    def __hash__(self):
        return hash((self.name, self.price))  # –ò—Å–ø–æ–ª—å–∑—É–µ–º –∫–æ—Ä—Ç–µ–∂

