class Product:
    def __init__(self, name, price):
        self.name = name
        self.price = price

    def __eq__(self, other):
        return self.price == other.price        # and self.name.lower() == other.name.lower()

    # def __ne__(self, other):
    #     return self.price != other.price

    # def __lt__(self, other):
